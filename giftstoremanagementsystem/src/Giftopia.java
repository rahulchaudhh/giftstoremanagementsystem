    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
 String customerName = name.getText();
    String orderDate = orderdate.getText();
    String phoneNumber = phonenumber.getText();
    String customerLocation = location.getText();
    String idNumber = idnumber.getText();

    // Check if any field is empty
    if (customerName.isEmpty() || orderDate.isEmpty() || phoneNumber.isEmpty() || customerLocation.isEmpty() || idNumber.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate that customer name is NOT numeric
    if (customerName.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Name must not be numeric!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate order date format (dd/mm/yyyy)
    if (!orderDate.matches("\\d{2}/\\d{2}/\\d{4}")) {
        JOptionPane.showMessageDialog(this, "Order date must be in the format dd/mm/yyyy!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate phone number (10 digits)
    if (!phoneNumber.matches("\\d{10}")) {
        JOptionPane.showMessageDialog(this, "Phone number must be exactly 10 digits!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate customer location is NOT numeric
    if (customerLocation.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Location must not be numeric!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate that the ID number is unique
    DefaultTableModel model = (DefaultTableModel) Table.getModel();
    for (int i = 0; i < model.getRowCount(); i++) {
        String existingIdNumber = model.getValueAt(i, 4).toString(); // Assuming ID number is in column 4 (index 4)
        if (existingIdNumber.equals(idNumber)) {
            JOptionPane.showMessageDialog(this, "ID Number already exists! Please use a unique ID.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    // Add new row to the table
    model.addRow(new Object[]{customerName, orderDate, phoneNumber, customerLocation, idNumber});

    // Clear input fields
    name.setText("");
    orderdate.setText("");
    phonenumber.setText("");
    location.setText("");
    idnumber.setText("");

    JOptionPane.showMessageDialog(this, "Record added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_addbtnActionPerformed

